public class Value {

    private int id;



    private String number;
    private String name;

    public Value(String a) {
        this.number = number;
        this.name = a;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {

        return id;
    }

    public String getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }


}
